//program to display 5 times Hello on screen 


# include<stdio.h>

//Demonstration of ITERATION

void Display()   //Defination
{
    
   int iCnt = 0;  
   
   //1 Intialization
   //2 condition
   //3 Displacement
   //4 Loop body

    //         1             2           3
    for(iCnt =1; iCnt <= 5 ;  iCnt++  ) 
     {
        printf("Hello\n");    //4
     }   

}

 int main()
{
   Display();     //function call
   
   return 0;
}



   